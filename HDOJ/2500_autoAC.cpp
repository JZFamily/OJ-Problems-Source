#include<stdio.h>
int main()
{
    int n;
    scanf("%d",&n);
    while(n--)
    {
        int k,i=0,j=0;
        scanf("%d",&k);
        for(i=0;i<k*3;i++)
        {
            for(j=0;j<k;j++)
                {
                    printf("HDU");
                }
            printf("\n");
        }
    }
    return 0;
}
